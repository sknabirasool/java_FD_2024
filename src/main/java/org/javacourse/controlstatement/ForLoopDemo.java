package org.javacourse.controlstatement;

public class ForLoopDemo {

    public static void main(String[] args) {

        /**
         * for(intialization:condition:inc/dec){}
         */

        /**
         * I want to print numbers 1 to 100
         */
        for (int i=1;i<=100;i++)
        {
            System.out.println(i);
        }
    }
}
